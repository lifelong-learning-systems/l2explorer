import numpy as np

"""
Predefined maps defined in L2Explorer Unity

Each map is 200x200 meters total, including out-of-bounds space (such as 
the area outside the walls in Craters). Coordinate (0,0) is at the center of the map. 
x, y axes increase towards the top right corner and decrease towards the bottom left so that:

(-100, 100)	      (100, 100)
            (0,0)	
(-100, -100)      (100, -100)
"""

"""
can manually define the polygons like this:
    polygon_specs = [
        np.array([(20,50),(20,100),(80,100),(80,50)]),
        np.array([(120,20),(120,80),(135,60),(150,80),(150,20)])
    ]
"""

_maps = []

def get_map(map_id):
    map = next( (m for m in _maps if m['id'] == map_id), None)
    if not map:
        raise NameError("Not a valid map: {0}".format(map_id))
    polygon_specs = []
    for poly in map['excluded_regions']:
        polygon_specs.append(np.array(poly))
    return polygon_specs, map['xlimits'], map['ylimits']

